/*
分层说明：
==========

1. 标准库层：
   - 提供基础的C语言功能

2. 硬件抽象层(HAL)：
   - 提供对底层硬件的抽象接口
   - 包含芯片配置、定时器、通信接口等

3. 设备驱动层：
   - 针对具体硬件设备的驱动程序
   - 按功能分类：显示、传感器、执行器、通信、音视觉

4. 中间件层：
   - 提供通用的服务和协议
   - 任务调度、通信协议等

5. 应用组件层：
   - 对驱动层的封装，提供更高级的接口
   - 包含用户接口和功能组件

6. 业务逻辑层：
   - 实现具体的业务逻辑
   - 小车控制和状态管理

7. 应用层：
   - 最顶层的应用程序
   - 用户界面、测试、调试等

*/